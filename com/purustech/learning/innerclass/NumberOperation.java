package com.purustech.learning.innerclass;

public class NumberOperation {
        public int addNumber(TwoNumberHolder twoNumberHolder){
           return twoNumberHolder.x - twoNumberHolder.y;
        }
        public int subtractNumber(TwoNumberHolder twoNumberHolder){
            return twoNumberHolder.x - twoNumberHolder.y;
        }
        public int multiplyNumber(TwoNumberHolder twoNumberHolder){
            return twoNumberHolder.x * twoNumberHolder.y;
        }
}
